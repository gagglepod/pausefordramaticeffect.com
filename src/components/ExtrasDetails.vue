          <h3 class="extras-detail__subheading">Writing Process</h3>
          <p class="extras-detail__info">
            The story of PAUSE for Dramatic Effect started as a pre-selected, one-word
            writing prompt challenge called "Inktober" in October 2019. Each word in the
            prompt inspired the title and script for what would become an episode.
          </p>
          <p class="extras-detail__info">
            The 30 prompt words I used to write a part of the story each day in October, I
            combine several of them to form 17 chapters of a complete season. Then I took
            those 17 chapters and crafted 17 audio drama scripts, some written the day
            before (and sometimes day-of) recording that episode. With no time for
            re-recording or script edits, whatever I wrote became the dialogue, sound
            design, and plot for that episode.
          </p>
          <p class="extras-detail__info">
            The end result was a "pantsers" dream come true, complete with epic drama,
            cringy moments, and a lot of happy accidents. I wouldn't trade the experience
            for anything!
          </p>
